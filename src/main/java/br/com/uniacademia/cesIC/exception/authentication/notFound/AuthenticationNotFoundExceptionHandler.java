package br.com.uniacademia.cesIC.exception.authentication.notFound;

import org.springframework.http.HttpStatus;
import org.springframework.http.ResponseEntity;
import org.springframework.web.bind.annotation.ControllerAdvice;
import org.springframework.web.bind.annotation.ExceptionHandler;
import org.springframework.web.servlet.mvc.method.annotation.ResponseEntityExceptionHandler;

import br.com.uniacademia.cesIC.util.Response;
import lombok.extern.slf4j.Slf4j;

@Slf4j
@ControllerAdvice
public class AuthenticationNotFoundExceptionHandler extends ResponseEntityExceptionHandler {

	@ExceptionHandler(AuthenticationNotFoundException.class)
	public final ResponseEntity<Object> exceptionHandler(AuthenticationNotFoundException exception) {
		log.error("AuthenticationNotFoundException - Message: {}", exception);

		Response<Void> response = new Response<>();
		response.addError(exception.getMessage());

		return ResponseEntity.status(HttpStatus.NOT_FOUND).body(response);
	}
}
